function NestedList({ items }: { items: Node[] }) {
  return (
    <ol className={ps.nestedOl}>
      {items.map((item, idx) => (
        <li key={idx} className={ps.nestedItem}>
          <span className={ps.nestedItemText}>{item.text}</span>
          {item.children && item.children.length > 0 && <NestedList items={item.children} />}
          {item.bullets && item.bullets.length > 0 && (
            <ul className={ps.bulletList}>
              {item.bullets.map((b, bIdx) => (
                <li key={bIdx} className={ps.bulletItem}>{b}</li>
              ))}
            </ul>
          )}
        </li>
      ))}
    </ol>
  );
}

